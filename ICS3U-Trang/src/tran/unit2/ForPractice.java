/**
 * This program is a practice program with for loops.<br>
 * It will print hello 10 times using a for loop.<br><p>
 * Date: November 11, 2019
 * @author Trang Tran
 */
public class ForPractice {
	/**
	 * Start of the program
	 */
	public static void main(String[] args) {
		
		//prints hello every time it meets the condition
		for (int i=1;i<=10;i++)
		{
			System.out.println("Hello!");
		}//end for loop

	}

}
